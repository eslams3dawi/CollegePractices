ORG 100H

LEA SI, DATAP
MOV AL, 0
MOV CX, 128

SAVEP:
MOV [SI], AL
INC AL
INC SI
LOOP SAVEP

LEA DI, DATAN
MOV AL, -128
MOV CX, 128

SAVEN:
MOV [DI], AL
INC AL
INC DI
LOOP SAVEN

 
LEA SI, DATAP
LEA DI, DATAN
LEA BX, DATAM
MOV CX, 128

MULTIPLY:
MOV AL, [SI]
IMUL [DI]
MOV [BX], AX
INC SI
INC DI
INC BX
LOOP MULTIPLY



RET
DATAP DB 128 DUP(?)
DATAN DB 128 DUP(?)
DATAM DW 128 DUP(?)